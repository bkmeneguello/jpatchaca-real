package com.meneguello.jira;

public class User {

	private String fullname;
	
	private String name;

	void setFullname(String fullname) {
		this.fullname = fullname;
	}
	
	public String getFullname() {
		return fullname;
	}

	void setName(String name) {
		this.name = name;
	}
	
	public String getName() {
		return name;
	}

}
